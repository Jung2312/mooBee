package movie;

public class MovieBean {

	private int docid;
	private String movieSeq;
	private String title;
	private String directorNm;
	private String actorNm;
	private String plot;
	private String genre;
	private String releaseDate;
	private String rubtime;
	private String posterUrl;
	private String vodUrl;
	private String audiAcc;
	private String rating;
	
	public int getDocid() {
		return docid;
	}



	public void setDocid(int docid) {
		this.docid = docid;
	}



	public String getMovieSeq() {
		return movieSeq;
	}



	public void setMovieSeq(String movieSeq) {
		this.movieSeq = movieSeq;
	}



	public String getTitle() {
		return title;
	}



	public void setTitle(String title) {
		this.title = title;
	}



	public String getDirectorNm() {
		return directorNm;
	}



	public void setDirectorNm(String directorNm) {
		this.directorNm = directorNm;
	}



	public String getActorNm() {
		return actorNm;
	}



	public void setActorNm(String actorNm) {
		this.actorNm = actorNm;
	}



	public String getPlot() {
		return plot;
	}



	public void setPlot(String plot) {
		this.plot = plot;
	}



	public String getGenre() {
		return genre;
	}



	public void setGenre(String genre) {
		this.genre = genre;
	}



	public String getReleaseDate() {
		return releaseDate;
	}



	public void setReleaseDate(String releaseDate) {
		this.releaseDate = releaseDate;
	}



	public String getRubtime() {
		return rubtime;
	}



	public void setRubtime(String rubtime) {
		this.rubtime = rubtime;
	}



	public String getPosterUrl() {
		return posterUrl;
	}



	public void setPosterUrl(String posterUrl) {
		this.posterUrl = posterUrl;
	}



	public String getVodUrl() {
		return vodUrl;
	}



	public void setVodUrl(String vodUrl) {
		this.vodUrl = vodUrl;
	}



	public String getAudiAcc() {
		return audiAcc;
	}



	public void setAudiAcc(String audiAcc) {
		this.audiAcc = audiAcc;
	}



	public String getRating() {
		return rating;
	}



	public void setRating(String rating) {
		this.rating = rating;
	}

}
